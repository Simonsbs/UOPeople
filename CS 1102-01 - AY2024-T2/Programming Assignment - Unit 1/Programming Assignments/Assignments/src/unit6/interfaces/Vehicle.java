package unit6.interfaces;

public interface Vehicle {

    // Method to get the make of the vehicle
    String getMake();

    // Method to set the make of the vehicle
    void setMake(String make);

    // Method to get the model of the vehicle
    String getModel();

    // Method to set the model of the vehicle
    void setModel(String model);

    // Method to get the year of manufacture of the vehicle
    int getYearOfManufacture();

    // Method to set the year of manufacture of the vehicle
    void setYearOfManufacture(int yearOfManufacture);
}
